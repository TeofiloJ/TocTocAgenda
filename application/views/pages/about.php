        Dans le cadre des projets Ydays au sein de l’école Ingésup de Nantes, nous avons l’opportunité de réaliser un projet d’étude. Ce projet est à réaliser en groupe de plusieurs étudiants venant des différentes formations et il consiste à développer une solution web visant à faciliter la gestion des biens immobiliers.
Objectif

Le but du projet est de faciliter la gestion des rendez-vous lors d’une recherche ou d’une vente d’un bien immobilier que ce soit pour les particuliers ou les agences immobilières. 
De plus, une intelligence artificielle permettrait de répondre aux questions fréquentes des utilisateurs.

Pour les particuliers et les agences immobilières, l’objectif principal est de faciliter la gestion des rendez-vous et de simplifier l’échange d’informations via un système d’agenda .

En plus de cet objectif, l’analyse de la donnée vient s’appuyer afin d’avoir une vision statistique multidimensionnelle.
